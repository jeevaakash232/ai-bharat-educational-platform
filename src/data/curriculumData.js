// Get curriculum data for specific class and subject
export const getCurriculumData = (className, subject) => {
  const classKey = `class${className}`
  const subjectKey = subject.toLowerCase().replace(/\s+/g, '')
  
  return curriculumData[classKey]?.[subjectKey] || null
}

// Get subject-specific question types
export const getSubjectQuestionTypes = (subject) => {
  const subjectKey = subject.toLowerCase().replace(/\s+/g, '')
  return subjectQuestions[subjectKey] || subjectQuestions.general
}

// Check if question is subject-specific
export const isSubjectQuestion = (question, subject) => {
  const subjectData = getSubjectQuestionTypes(subject)
  if (!subjectData) return false
  
  const questionLower = question.toLowerCase()
  return subjectData.examples.some(example => 
    questionLower.includes(example.toLowerCase().split(' ')[0])
  )
}